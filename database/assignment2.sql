-- Create review table
CREATE TABLE public.review (
  review_id     INT GENERATED BY DEFAULT AS IDENTITY,
  review_text   TEXT NOT NULL,
  review_rating INT NOT NULL CHECK (review_rating BETWEEN 1 AND 5),
  inv_id        INT NOT NULL,
  account_id    INT NOT NULL,
  review_date   TIMESTAMP NOT NULL DEFAULT NOW(),
  CONSTRAINT review_pkey PRIMARY KEY (review_id),
  CONSTRAINT fk_review_inv FOREIGN KEY (inv_id)
    REFERENCES public.inventory(inv_id) ON DELETE CASCADE,
  CONSTRAINT fk_review_account FOREIGN KEY (account_id)
    REFERENCES public.account(account_id) ON DELETE CASCADE
);